import Foundation
import UIKit

extension UIColor {
    class func mainColor() -> UIColor {
        return UIColor(red: 0.24, green: 0.36, blue: 1.00, alpha: 1.00)
    }
    
    class func titleColor() -> UIColor {
        return UIColor(red: 0.12, green: 0.12, blue: 0.22, alpha: 1.00)
    }
    
    class func contentColor() -> UIColor {
        return UIColor(red: 0.52, green: 0.52, blue: 0.59, alpha: 1.00)
    }
    
    class func borderColor() -> UIColor {
        return UIColor(red: 0.72, green: 0.72, blue: 0.82, alpha: 1.00)
    }
    
    class func iconColor() -> UIColor {
        return UIColor(red: 0.96, green: 0.95, blue: 0.99, alpha: 1.00)
    }
}
